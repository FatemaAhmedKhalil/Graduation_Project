u8 DigitalSensor_u8Sensing (DigitalSensor Sensor)
{
	u8 u8SesnsingState = 0;
	
	GPIO_u8GetPinValue (Sensor.PORT, Sensor.PIN, &u8SesnsingState);
	
	return u8SesnsingState;
}
